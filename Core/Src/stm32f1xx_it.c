/**
 * @brief This function handles USART2 global interrupt.
 */
void USART2_IRQHandler(void)
{
  /* USER CODE BEGIN USART2_IRQn 0 */
  uint8_t data;

  if (__HAL_UART_GET_FLAG(&huart2, UART_FLAG_RXNE) &&
      __HAL_UART_GET_IT_SOURCE(&huart2, UART_IT_RXNE))
  {
    data = (uint8_t)(huart2.Instance->DR & 0x00FF);

    if (WIFI_RX_LEN < WIFI_RX_BUFFER_SIZE - 1)
    {
      WIFI_RX_BUFFER[WIFI_RX_LEN++] = data;

      // 检测行结束符
      if (data == '\n' && WIFI_RX_LEN >= 2 &&
          WIFI_RX_BUFFER[WIFI_RX_LEN - 2] == '\r')
      {
        WIFI_RX_FLAG = 1;
      }
    }
    else
    {
      // 缓冲区满，清空缓冲区
      Clear_WIFI_RX_Buffer();
    }
    __HAL_UART_CLEAR_FLAG(&huart2, UART_FLAG_RXNE);
  }
  else if (__HAL_UART_GET_FLAG(&huart2, UART_FLAG_IDLE) &&
           __HAL_UART_GET_IT_SOURCE(&huart2, UART_IT_IDLE))
  {
    __HAL_UART_CLEAR_IDLEFLAG(&huart2);
    // IDLE中断表示数据传输完成
    if (WIFI_RX_LEN > 0)
    {
      WIFI_RX_BUFFER[WIFI_RX_LEN] = '\0';
      WIFI_RX_FLAG = 1;
    }
  }
  /* USER CODE END USART2_IRQn 0 */
  HAL_UART_IRQHandler(&huart2);
  /* USER CODE BEGIN USART2_IRQn 1 */

  /* USER CODE END USART2_IRQn 1 */
}

